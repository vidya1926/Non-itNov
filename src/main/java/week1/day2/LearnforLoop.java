package week1.day2;

public class LearnforLoop {

	public static void main(String[] args) {
	
		
		for(int n=1;n<=10;n++) {
			
			System.out.println(n);
			//1
			//1+1=2
		}
		
		//System.out.println("TO print in reverse order");
		
		for(int r=10;r>0;r--) {
			System.out.println(r);
			
			//10
			//10-1
		}
		
		int input=5;
		//5*4*3*2*1
		int f=1;
		for(int i=1;i<=input;i++) {
			f=f*i; //1*1
					
		}
		
		System.out.println(f);
		
		
		
		
		
		
		
		
		
		
		
	}

}
